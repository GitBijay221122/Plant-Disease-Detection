const LANGUAGE_NAMES = {
  en: 'English',
  hi: 'हिंदी',
  bn: 'বাংলা',
  ta: 'தமிழ்',
  te: 'తెలుగు',
  es: 'Español',
  fr: 'Français',
  ar: 'العربية',
};

/* RTL languages */
const RTL_LANGS = new Set(['ar']);

/* Current language — default English */
let currentLang = localStorage.getItem('leafscan_lang') || 'en';

function t(key) {
  return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key])
    || (TRANSLATIONS['en'] && TRANSLATIONS['en'][key])
    || key;
}

function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('leafscan_lang', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = RTL_LANGS.has(lang) ? 'rtl' : 'ltr';

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  // Update placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-ph'));
  });

  // Update language switcher active state
  document.querySelectorAll('.lang-option').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Update chatbot greeting language
  if (window._chatMessages && window._chatMessages.length === 0) {
    const greetEl = document.querySelector('.chat-greeting-msg');
    if (greetEl) greetEl.textContent = t('chat_greeting');
  }

  // Refresh severity labels if result is showing
  document.querySelectorAll('[data-sev-key]').forEach(el => {
    el.textContent = t(el.getAttribute('data-sev-key'));
  });
}
